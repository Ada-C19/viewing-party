def create_movie(title, genre, rating):
    movies_info = {}
    movies_info['title'] = title
    movies_info['genre'] = genre
    movies_info['rating'] = rating
    if not movies_info['title']or not movies_info['genre'] or not movies_info['rating']:
        return None
    return movies_info

