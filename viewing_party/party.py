def create_movie(title, genre, rating): 
    # if any of the vars are None, return None
    if not title and not genre and not rating:
        return None
    
    # create new dict
    new_movie = {
        "title": title,
        "genre": genre,
        "rating":rating,
    }

    return new_movie


